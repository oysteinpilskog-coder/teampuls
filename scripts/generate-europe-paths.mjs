// One-off codegen: reads a TopoJSON world atlas and emits a TypeScript file
// with pre-projected SVG path data for European countries, using the same
// Mercator-ish projection as src/lib/geo.ts.
//
// Usage:
//   node scripts/generate-europe-paths.mjs <path-to-countries-110m.json>
//
// Writes to: src/lib/europe-paths.ts

import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const ROOT = path.resolve(__dirname, '..')

const input = process.argv[2]
if (!input) {
  console.error('Usage: node scripts/generate-europe-paths.mjs <topojson>')
  process.exit(1)
}
const topo = JSON.parse(fs.readFileSync(input, 'utf8'))

// ── Mirror of lib/geo.ts projection ──────────────────────────────────
const BOUNDS = { latMin: 35, latMax: 72, lngMin: -12, lngMax: 32 }
const MAP_W = 1400
const MAP_H = 900

function mercatorY(latDeg) {
  const lat = (latDeg * Math.PI) / 180
  return Math.log(Math.tan(Math.PI / 4 + lat / 2))
}
function project(lat, lng) {
  const { latMin, latMax, lngMin, lngMax } = BOUNDS
  const x = ((lng - lngMin) / (lngMax - lngMin)) * MAP_W
  const yMax = mercatorY(latMax)
  const yMin = mercatorY(latMin)
  const y = ((yMax - mercatorY(lat)) / (yMax - yMin)) * MAP_H
  return [x, y]
}

// ── Minimal TopoJSON → GeoJSON decoder ───────────────────────────────
const { transform, arcs: rawArcs, objects } = topo
const [sx, sy] = transform.scale
const [tx, ty] = transform.translate

// Decode arcs: delta-decoded [x, y] → absolute [lng, lat]
const arcs = rawArcs.map(arc => {
  let x = 0, y = 0
  return arc.map(([dx, dy]) => {
    x += dx
    y += dy
    return [x * sx + tx, y * sy + ty]
  })
})

function resolveArcs(indices) {
  const ring = []
  indices.forEach((i, ai) => {
    const rev = i < 0
    const arc = arcs[rev ? ~i : i]
    const pts = rev ? [...arc].reverse() : arc
    // Avoid duplicate join point
    const start = ai === 0 ? 0 : 1
    for (let k = start; k < pts.length; k++) ring.push(pts[k])
  })
  return ring
}

function geometryToPolygons(geom) {
  // Returns an array of polygons, each polygon = array of rings, ring = [[lng,lat],...]
  if (geom.type === 'Polygon') {
    return [geom.arcs.map(resolveArcs)]
  }
  if (geom.type === 'MultiPolygon') {
    return geom.arcs.map(poly => poly.map(resolveArcs))
  }
  return []
}

// ── Iterate countries ────────────────────────────────────────────────
const countriesObj = objects.countries
if (!countriesObj) {
  console.error('Expected objects.countries in topojson')
  process.exit(1)
}

// Generous Europe box for filtering (slightly larger than draw box so
// coastlines that extend outside the viewport still clip cleanly)
const FILTER = { latMin: 30, latMax: 78, lngMin: -25, lngMax: 45 }

function anyPointInBox(rings) {
  for (const ring of rings) {
    for (const [lng, lat] of ring) {
      if (
        lat >= FILTER.latMin && lat <= FILTER.latMax &&
        lng >= FILTER.lngMin && lng <= FILTER.lngMax
      ) return true
    }
  }
  return false
}

const results = []
for (const geom of countriesObj.geometries) {
  const name = geom.properties?.name ?? 'Unknown'
  const polys = geometryToPolygons(geom)

  // Keep polygons that intersect our filter box
  const keptPolys = polys.filter(p => anyPointInBox(p))
  if (keptPolys.length === 0) continue

  // Convert to SVG path data. Round to 0.5 px to keep the string short.
  let d = ''
  for (const poly of keptPolys) {
    for (const ring of poly) {
      if (ring.length < 3) continue
      ring.forEach(([lng, lat], i) => {
        const [x, y] = project(lat, lng)
        const rx = Math.round(x * 2) / 2
        const ry = Math.round(y * 2) / 2
        d += (i === 0 ? 'M' : 'L') + rx + ',' + ry
      })
      d += 'Z'
    }
  }
  if (!d) continue
  results.push({ name, d })
}

// Sort by name for deterministic output
results.sort((a, b) => a.name.localeCompare(b.name))

const header = `// AUTO-GENERATED by scripts/generate-europe-paths.mjs
// Source: natural-earth-data world-atlas 110m resolution
// Projection: Mercator (lat) + linear (lng), viewBox 0 0 ${MAP_W} ${MAP_H}
// Bounds: lat ${BOUNDS.latMin}–${BOUNDS.latMax}, lng ${BOUNDS.lngMin}–${BOUNDS.lngMax}
// DO NOT EDIT — regenerate with \`node scripts/generate-europe-paths.mjs <topojson>\`.

export interface CountryPath {
  name: string
  d: string
}

export const EUROPE_COUNTRY_PATHS: CountryPath[] = [
`

const body = results
  .map(r => `  { name: ${JSON.stringify(r.name)}, d: ${JSON.stringify(r.d)} },`)
  .join('\n')

const out = header + body + '\n]\n'

const outPath = path.join(ROOT, 'src/lib/europe-paths.ts')
fs.writeFileSync(outPath, out, 'utf8')
console.log(`Wrote ${results.length} countries → ${outPath} (${(out.length / 1024).toFixed(1)} KB)`)
